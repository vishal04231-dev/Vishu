public class VarDemo {
    public static void main(String[] args) {
        var number = 100;
        var price = 45.75;
        var name = "Kanishka";
        var isActive = true;

        System.out.println("Number: " + number);
        System.out.println("Price: " + price);
        System.out.println("Name: " + name);
        System.out.println("Is Active: " + isActive);
    }
}
