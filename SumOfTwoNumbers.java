public class SumOfTwoNumbers {
    public static void main(String[] args) {
        
        int num1 = 10;   // first number
        int num2 = 20;   // second number

       
        int sum = num1 + num2;

       
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
