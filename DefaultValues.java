public class DefaultValues {
    int a;
    double b;
    boolean c;
    char d;
    String e;

    public static void main(String[] args) {
        DefaultValues obj = new DefaultValues();

        System.out.println("Default value of int: " + obj.a);
        System.out.println("Default value of double: " + obj.b);
        System.out.println("Default value of boolean: " + obj.c);
        System.out.println("Default value of char: " + obj.d);
        System.out.println("Default value of String: " + obj.e);
    }
}
